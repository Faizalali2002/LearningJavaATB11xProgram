package ex_02_Operators;

public class Lab28_operators_arithmetic {
    public static void main(String[] args) {
        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

        int a = 20;
        int b = 3;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c); // 6.666665
        //if you divide something with float it will be float

        System.out.println("a+b");
        System.out.println(a+b);
    }
}
